    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        int filaSeleccionada = tablaEvaluaciones.getSelectedRow();
        if (filaSeleccionada >= 0) {
            EvaluacionDesempeno evaluacionSeleccionada = evaluacionControlador.getEvaluaciones().get(filaSeleccionada);
            ActualizarEvaluacionDialog dialog = new ActualizarEvaluacionDialog(this, true, evaluacionControlador, evaluacionSeleccionada);
            dialog.setVisible(true);
            cargarDatos();
        } else {
            JOptionPane.showMessageDialog(this, "Por favor, seleccione una evaluación para actualizar.");
        }
    }//GEN-LAST:event_btnActualizarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int filaSeleccionada = tablaEvaluaciones.getSelectedRow();
        if (filaSeleccionada >= 0){
            List<EvaluacionDesempeno> listaEvaluaciones = evaluacionControlador.getEvaluaciones();
            EvaluacionDesempeno evaluacionSeleccionada = listaEvaluaciones.get(filaSeleccionada);
            
            ActualizarEvaluacionDialog dialog = new ActualizarEvaluacionDialog(this, true, evaluacionControlador, evaluacionSeleccionada);
            dialog.setVisible(true);
            cargarDatos();
        } else {
            JOptionPane.showMessageDialog(this, "Por Favor, selecciona una evaluacion en la tabla para Actualizar");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

