    private void cargarDatos() {
        modelo.setRowCount(0);
        List<EvaluacionDesempeno> evaluaciones = evaluacionControlador.getEvaluaciones();
        for (EvaluacionDesempeno evaluacion : evaluaciones){
            modelo.addRow(new Object[]{
                evaluacion.getFecha(),
                evaluacion.getEmpleado(),
                evaluacion.getResultadoEvaluacion(),
                evaluacion.getComentarios()
            });
        }
    }
