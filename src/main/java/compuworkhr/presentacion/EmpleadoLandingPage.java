    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        int filaSeleccionada = tablaEmpleados.getSelectedRow();
        if (filaSeleccionada >= 0) {
            int id = (int) modelo.getValueAt(filaSeleccionada, 0);
            Empleado empleadoSeleccionado = empleadoCtrl.buscarPorId(id);
            abrirActualizarEmpleadoDialog(empleadoSeleccionado);
        } else {
            JOptionPane.showMessageDialog(this, "Por favor, seleccione un empleado para actualizar.");
        } 
    }//GEN-LAST:event_btnActualizarActionPerformed

