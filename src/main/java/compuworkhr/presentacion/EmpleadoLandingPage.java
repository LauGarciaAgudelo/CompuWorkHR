    private void inicializarTabla() {
        modelo = new DefaultTableModel(new Object[]{"ID", "Nombre", "Cargo", "Salario"}, 0);
        tablaEmpleados.setModel(modelo);
    }
    
    // MÃ©todo para cargar los datos de empleados en la tabla
    public void cargarDatos() {
        modelo.setRowCount(0); 
        for (Empleado emp : empleadoCtrl.getAllEmpleados()) {
            modelo.addRow(new Object[]{emp.getId(), emp.getNombre(), emp.getCargo(), emp.getSalario()});
    }
    }
