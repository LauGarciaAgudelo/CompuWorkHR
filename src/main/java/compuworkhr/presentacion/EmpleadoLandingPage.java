    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int filaSeleccionada = tablaEmpleados.getSelectedRow();
        if (filaSeleccionada >= 0) {
            int id = (int) modelo.getValueAt(filaSeleccionada, 0);
            try {
                empleadoCtrl.eliminarEmpleado(id);
                JOptionPane.showMessageDialog(this, "Empleado eliminado correctamente.");
                cargarDatos();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Error al eliminar empleado: " + e.getMessage());
            }
        } else {
            JOptionPane.showMessageDialog(this, "Por favor, seleccione un empleado para eliminar.");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

